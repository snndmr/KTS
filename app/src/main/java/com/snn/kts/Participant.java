package com.snn.kts;

public class Participant {
    public String id;
    public String name;
    public String email;
    public String phone;

    Participant() {
    }

    Participant(String id, String name, String email, String phone) {
        this.id = id;
        this.name = name;
        this.email = email;
        this.phone = phone;
    }

}
